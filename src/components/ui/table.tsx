const TableHead = React.forwardRef<
  HTMLTableCellElement,
  React.ThHTMLAttributes<HTMLTableCellElement>
>(({ className, ...props }, ref) => (
  <th
    ref={ref}
    className={cn(
      "h-10 text-left font-medium text-muted-foreground [&:has([role=checkbox])]:pr-0 [&>[role=checkbox]]:translate-y-[2px]",
      className
    )}
    {...props}
  />
));
TableHead.displayName = "TableHead";

type Variant = "income" | "outcome";

// Criamos um novo tipo que estende as propriedades padr√£o de <td> e adiciona `variant`
interface TableCellProps extends React.TdHTMLAttributes<HTMLTableCellElement> {
  variant?: Variant;
}

const TableCell = React.forwardRef<HTMLTableCellElement, TableCellProps>(
  ({ className, variant, ...props }, ref) => (
    <td
      ref={ref}
      className={cn(
        "p-0 align-middle [&:has([role=checkbox])]:pr-0 [&>[role=checkbox]]:translate-y-[2px]",
        variant === "income" && "text-green-500", // Exemplo de estilo para `income`
        variant === "outcome" && "text-red-500", // Exemplo de estilo para `outcome`
        className
      )}
      {...props}
    />
  )
);

TableCell.displayName = "TableCell";

